# example of relational operator
num1 = int(input("Enter 1st number")) #10
num2 = int(input("Enter 2nd number")) #20

result = num1 == num2 
print(f"{result} = {num1} == {num2}")

result = num1 != num2
print(f"{result} = {num1} != {num2}")

result = num1 < num2
print(f"{result} = {num1} < {num2}")

result = num1 > num2
print(f"{result} = {num1} > {num2}")

result = num1 <= num2
print(f"{result} = {num1} <= {num2}")

result = num1 >= num2
print(f"{result} = {num1} >= {num2}")
