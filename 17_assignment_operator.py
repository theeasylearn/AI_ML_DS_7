# assignment operators in python 
num1 = int(input("Enter 1st number")) #10
num2 = int(input("Enter 2nd number")) #20

# num1 = num1 + num2 
num1 += num2
print(num1)
# num1 = num1 - num2 
num1 -= num2
print(num1)
# num1 = num1 * num2 
num1 *= num2
print(num1)
# num1 = num1 / num2 
num1 /= num2
print(num1)
# num1 = num1 ** num2 
num1 **= num2
print(num1)
# num1 = num1 % num2 
num1 %= num2
print(num1)
# num1 = num1 // num2 
num1 //= num2
print(num1)